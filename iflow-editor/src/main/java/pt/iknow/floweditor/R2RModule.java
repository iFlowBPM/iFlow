package pt.iknow.floweditor;

public class R2RModule {
  private String nameFlowXml;
  private String installFlowXml;
  private String uninstallFlowXml;
  private String applicationFlowXml;

  public String getInstallFlowXml() {
    return installFlowXml;
  }

  public void setInstallFlowXml(String installFlowXml) {
    this.installFlowXml = installFlowXml;
  }

  public String getUninstallFlowXml() {
    return uninstallFlowXml;
  }

  public void setUninstallFlowXml(String uninstallFlowXml) {
    this.uninstallFlowXml = uninstallFlowXml;
  }

  public String getApplicationFlowXml() {
    return applicationFlowXml;
  }

  public void setApplicationFlowXml(String applicationFlowXml) {
    this.applicationFlowXml = applicationFlowXml;
  }

  public void setNameFlowXml(String nameFlowXml) {
    this.nameFlowXml = nameFlowXml;
  }

  public String getNameFlowXml() {
    return nameFlowXml;
  }
  
}
